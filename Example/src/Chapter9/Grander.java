package Chapter9;

public class Grander extends Car{

	@Override
	public void start() {
		System.out.println("Grander 시동을 켭니다.");
		
	}

	@Override
	public void drive() {
		System.out.println("Grander 달립니다.");
		
	}

	@Override
	public void stop() {
		System.out.println("Grander 멈춥니다.");
		
	}

	@Override
	public void turnOff() {
		System.out.println("Grander 시동을 끕니다.");
		
	}

	
}
